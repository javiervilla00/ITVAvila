package com.javiervilla00.domain;

public class Motorcycle {
    public String plateNumber;
    public String brand;
    public String model;
    public String colour;
    public String plateYear;
    public String wheelDiameter;

    public String getPlateNumber() {
        return plateNumber;
    }

    public void setPlateNumber(String plateNumber) {
        this.plateNumber = plateNumber;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getColour() {
        return colour;
    }

    public void setColour(String colour) {
        this.colour = colour;
    }

    public String getPlateYear() {
        return plateYear;
    }

    public void setPlateYear(String plateYear) {
        this.plateYear = plateYear;
    }

    public String getWheelDiameter() {
        return wheelDiameter;
    }

    public void setWheelDiameter(String wheelDiameter) {
        this.wheelDiameter = wheelDiameter;
    }
}
